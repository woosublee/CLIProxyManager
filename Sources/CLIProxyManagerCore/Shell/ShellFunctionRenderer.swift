import Foundation

public enum ShellFunctionRendererError: Error, Equatable {
    case invalidFunctionName(String)
    case invalidPort(Int)
}

public struct ShellFunctionRenderer: Sendable {
    private let config: AppConfig
    private let helperCommand: String

    public init(config: AppConfig, helperCommand: String) {
        self.config = config
        self.helperCommand = helperCommand
    }

    public func render() throws -> String {
        try validate(functionName: config.commands.cc)
        try validate(functionName: config.commands.ccapi)
        try validate(functionName: config.commands.ccodex)
        try validate(port: config.port)

        let claudeCommand = config.includeDangerouslySkipPermissions
            ? "claude --dangerously-skip-permissions \"$@\""
            : "claude \"$@\""
        let port = config.port
        let helperCommand = shellSingleQuoted(helperCommand)

        return """
        # Generated by CLIProxyAPI Manager. Do not edit this file manually.
        # Edit profiles in the CLIProxyAPI Manager app instead.

        \(config.commands.cc)() {
          \(claudeCommand)
        }

        \(config.commands.ccapi)() {
          local anthropic_auth_token
          if ! anthropic_auth_token="$( \(helperCommand) secret get claude-api-key )"; then
            echo "Claude API key를 읽을 수 없습니다. CLIProxy Manager에서 API key를 저장해 주세요."
            return 1
          fi

          ANTHROPIC_AUTH_TOKEN="$anthropic_auth_token" \\
          ANTHROPIC_MODEL=\(shellSingleQuoted(config.ccapi.model)) \\
          \(claudeCommand)
        }

        \(config.commands.ccodex)() {
          if ! curl -sf "http://127.0.0.1:\(port)/v1/models" >/dev/null; then
            echo "CLIProxyAPI Manager가 실행 중이 아닙니다. 앱을 열어 주세요."
            return 1
          fi

          ANTHROPIC_BASE_URL="http://127.0.0.1:\(port)" \\
          ANTHROPIC_AUTH_TOKEN='sk-dummy' \\
          ANTHROPIC_DEFAULT_OPUS_MODEL=\(shellSingleQuoted(config.ccodex.opusModel)) \\
          ANTHROPIC_DEFAULT_SONNET_MODEL=\(shellSingleQuoted(config.ccodex.sonnetModel)) \\
          ANTHROPIC_DEFAULT_HAIKU_MODEL=\(shellSingleQuoted(config.ccodex.haikuModel)) \\
          \(claudeCommand)
        }

        """
    }

    private func validate(functionName: String) throws {
        guard let firstCharacter = functionName.first else {
            throw ShellFunctionRendererError.invalidFunctionName(functionName)
        }

        let firstCharacterIsValid = firstCharacter.isASCII && (firstCharacter.isLetter || firstCharacter == "_")
        let remainingCharactersAreValid = functionName.dropFirst().allSatisfy { character in
            character.isASCII && (character.isLetter || character.isNumber || character == "_")
        }

        guard firstCharacterIsValid && remainingCharactersAreValid else {
            throw ShellFunctionRendererError.invalidFunctionName(functionName)
        }
    }

    private func validate(port: Int) throws {
        guard (1...65_535).contains(port) else {
            throw ShellFunctionRendererError.invalidPort(port)
        }
    }

    private func shellSingleQuoted(_ value: String) -> String {
        if value.isEmpty { return "''" }
        return "'" + value.replacingOccurrences(of: "'", with: "'\\''") + "'"
    }
}
